package ejercicio2;

public class Objeto {
	private float valor;
	private float peso;
	
	public Objeto(float valor, float peso) {
		super();
		this.valor = valor;
		this.peso = peso;
	}

	public float getValor() {
		return valor;
	}

	public void setValor(float valor) {
		this.valor = valor;
	}

	public float getPeso() {
		return peso;
	}

	public void setPeso(float peso) {
		this.peso = peso;
	}
	
	
	
}
